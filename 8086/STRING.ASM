INPUTSTRING MACRO MSG 
LOCAL L1,L2
L2:MOV AH,01H
   INT 21H
   CMP AL,0DH
JE L1
   MOV [SI],AL
   INC SI
   JMP L2
L1:MOV AL,'$'
   MOV [SI],AL
 
ENDM

DISPMSG MACRO MSG
LEA DX,MSG
MOV AH,09H
INT 21H
ENDM

DISPNUM MACRO NUM
LOCAL L6,N1,L7,N2
MOV AL,NUM
MOV CL,O4H
SHR AL,CL
CMP AL,09H
JLE L6
ADD AL,37H
JMP N1
L6:ADD AL,30H
N1:MOV AH,02H
    INT 21H
MOV AL,NUM
AND AL,0F
CMP AL,09H
JLE L7
ADD AL,37H
JMP N2
L7:ADD AL,30H
N2:MOV AH,02H
   INT 21H

ENDM


DATA SEGMENT
    MSG1 DB "ENTER THE STRING$"
    MSG2 DB "ENTER THE CHARACTER TO DELETE$"
    MSG3 DB "DELETED STRING$"
    MSG4 DB " NUMBER OF OCCURENCE OF DELETED CHARACTER$"
    CHAR DB ?
     STRING DB 15
     ARR DB 15 
DATA ENDS
CODE SEGMENT
   ASSUME CS:CODE,DS:DATA
   START:MOV AX,DATA
         MOV DS,AX

         DISPMSG MSG1
         INPUTSTRING STRING

         DISPMSG MSG2
         MOV AH,01H
         INT 21H
         MOV BH,AL

    
         LEA DI,ARR
         MOV CH,00H
     L4: LEA SI,STRING
         CMP [SI],24H
         JE L5
         CMP [SI],BH
         JE L3
         
         MOV [DI],[SI]
         INC SI
         INC DI
         JMP L4
      L3:INC SI
         INC CH
         JMP L4
      L5:MOV [DI],24H
      MOV CHAR,CH

         DISPMSG MSG4
         DISPNUM CHAR

         DISPMSG  MSG3
         DISPMSG  ARR
  CODE ENDS
  END START
